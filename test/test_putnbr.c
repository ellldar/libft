#include "libft.h"

void	test(int n)
{
	ft_putnbr(n);
	ft_putchar('\n');
}

int	main(void)
{
	test(2147483647);
	test(12345);
	test(-2147483648);
	test(-147483648);
	test(-12345);
	test(0);
	test(+0);
	test(-0);
	test(1);
	test(12);
	test(123);
	test(1234);
	test(-1);
	test(-12);
	test(-123);
	test(-1234);
	test(10);
	test(100);
	test(1000);
	test(10000);
	test(100000);
	test(00001000);
	return (0);
}